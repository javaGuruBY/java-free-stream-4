package javaGuru.Demo;

public class mathOperationsDemo {
    public static void main(String[] args) {
        System.out.println("Welcome to the 'Math Operations'!");
        int a = 20;
        int b = 5;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("Addition '+':");   
        int sum = a + b;
        System.out.println("a + b = " + a + " + " + b + " = " + sum);
        System.out.println("Substraction '-':");
        int dif = a - b;
        System.out.println("a - b = " + a + " - " + b + " = " + dif);
        System.out.println("Multiplication '*':");
        int multipl = a * b;
        System.out.println("a * b = " + a + " * " + b + " = " + multipl);
        System.out.println("Division '/':");
        int div = a / b;
        System.out.println("a / b = " + a + " / " + b + " = " + div);// write your code here
    }
}
